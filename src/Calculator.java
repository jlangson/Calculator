public class Calculator {
    //add 2 ints
    public static int add(int a, int b){
        return a+b;
    }
    //subtract 2 ints
    public static int subtract(int a, int b){
        return a-b;
    }
    //multpy two ints
    public static int multiply(int a, int b){
        return a*b;
    }
    //divide two ints
    public static int divide(int a, int b){
        return a / b;
    }
    //square an int
    public static int square(int a){
        return multiply(a, a);
    }

}
